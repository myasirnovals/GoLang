package main

import "fmt"

func main() {
    
    a := 62
    b := 51
    
    var hasil int
    
    hasil = a + b
    fmt.Println("Hasil dari a + b = ", hasil, "\n")
    
    hasil = a - b
    fmt.Println("Hasil dari a - b = ", hasil, "\n")
    
    hasil = a * b
    fmt.Println("Hasil dari a * b = ", hasil, "\n")
    
    hasil = a / b
    fmt.Println("Hasil dari a / b = ", hasil, "\n")
    
    hasil = a % b
    fmt.Println("Hasil dari a % b = ", hasil, "\n")
}
